package Day6;

public class ForStarExample {

	public static void main(String[] args) {
		
		//for+ctrl space 2번째
		for (int i = 0; i < 5; i++) {
			for (int j = 0; j < i+1; j++) {
				System.out.print("*");
			}
			System.out.println();
			// 피라미드 부분은 알고리즘 문제이기 때문에 잠깐 보류하고
			// 다음 진도를 나가겠다. 일단 모두 봐야한다. 1/13

	}

}
}
